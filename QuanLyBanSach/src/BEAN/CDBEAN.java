package BEAN;

public class CDBEAN {
	String maCD;
	String tenCD;
	public CDBEAN(String maCD, String tenCD) {
		super();
		this.maCD = maCD;
		this.tenCD = tenCD;
	}
	public String getMaCD() {
		return maCD;
	}
	public void setMaCD(String maCD) {
		this.maCD = maCD;
	}
	public String getTenCD() {
		return tenCD;
	}
	public void setTenCD(String tenCD) {
		this.tenCD = tenCD;
	}


}
